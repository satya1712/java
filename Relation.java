public class Relation
{
    public static void main(String args[])
    {
        int a=3,b=3;
        System.out.println(a==b);
        System.out.println(a!=b);
        System.out.println(a>=b);
        System.out.println(a<=b);
        System.out.println(a>b);
        System.out.println(a<b);
    }
}